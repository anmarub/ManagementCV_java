    private void LlenarCombobox(){
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        int index = 0;
        modelo.addElement("Seleccione una Opcion");
        for(clsCities ciudad : listaCiudades){
            String data = ciudad.getNameCity();
            modelo.addElement(data);
            index++;
        }
        cbxCiudad.setModel(modelo);
        
    }
    
    
    private boolean llamarCamposEmpresa(int codigo){

        clsBusiness empresa = (clsBusiness) ctlEmpresa.searchEmpresa(codigo);
        
        if (empresa == null){
            JOptionPane.showMessageDialog(this, "Base de datos vacia, por favor diligencie la empresa", "Ingresar Informacion Empresa", 2);
            return true;
        }else{

            txtNit.setText(empresa.getNit());
            txtRazonSocial.setText(empresa.getNameBusiness());
            txtDireccion.setText(empresa.getAddress());
            cbxCiudad.setSelectedItem(empresa.getCity());
            return false;
        }
    
    }
    
    

