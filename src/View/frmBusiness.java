    private void btnBusinessAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBusinessAceptarActionPerformed

        if(txtNit.getText().equals("") || txtDireccion.getText().equals("")){
            try {
                String nit = txtNit.getText();
                String razonSocial = txtRazonSocial.getText();
                String direccion = txtDireccion.getText();
                String ciudad = cbxCiudad.getSelectedItem().toString();
                
                if (nit.equals("") || (razonSocial.equals("")) || (direccion.equals("")) || (ciudad.equals(""))){
                    JOptionPane.showMessageDialog(this, "Por favor diligencie todos los campos del formulario", "Informacion Incompleta", 1);
                }else{
                    clsBusiness Empresa = new clsBusiness(0, nit, razonSocial, direccion, ciudad);
                    ctlEmpresa.createEmpresa(Empresa);
                    JOptionPane.showMessageDialog(this, "Informacion registrada correctamente", "Registro Existoso", 1);
                }
            }catch(Exception error){
                JOptionPane.showMessageDialog(this, "Por favor agrege un Año Correcto", "Erro Año Mascota", 1);
            }

        }else{
            try {
                clsBusiness empresa = null;
                String nit = txtNit.getText();
                String razonSocial = txtRazonSocial.getText();
                String direccion = txtDireccion.getText();
                String ciudad = cbxCiudad.getSelectedItem().toString();
                
                    clsBusiness actualizarEmpresa = new clsBusiness(1, nit, razonSocial, direccion, ciudad);
                    ctlEmpresa.updateEmpresa(actualizarEmpresa);
                    JOptionPane.showMessageDialog(this, "Informacion registrada correctamente", "Registro Existoso", 1);
                
            }catch(Exception error){
                JOptionPane.showMessageDialog(this, "Por favor agrege un Año Correcto", "Erro Año Mascota", 1);
            }
        }
        
        
        
    }//GEN-LAST:event_btnBusinessAceptarActionPerformed

